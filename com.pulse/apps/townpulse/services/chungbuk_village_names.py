def village_name_for(sigungu: str, index: int) -> str:
    names = CHUNGBUK_NAMES.get(sigungu)
    if not names:
        return f"{sigungu}지역{index + 1}"
    return names[index % len(names)]


def is_placeholder_village_name(name: str) -> bool:
    return "읍면동" in name and any(ch.isdigit() for ch in name)
